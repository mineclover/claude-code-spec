/**
 * Sessions IPC Handlers
 * Dispatches to the correct session service based on toolId
 */

import { BrowserWindow, shell } from 'electron';
import {
  getAllClaudeProjects,
  getAllClaudeProjectsPaginated,
  getAutoGeneratedRequests,
  getClaudeProjectDir,
  getClaudeProjectsDir,
  getProjectSessionCount,
  getProjectSessions,
  getProjectSessionsBasic,
  getProjectSessionsPaginated,
  getSessionMetadata,
  getSessionPreview,
  getSessionSummary,
  getTotalProjectCount,
  getUserQuestions,
  readSessionLog,
} from '../../services/claudeSessions';
import { sessionAnalyticsService } from '../../services/SessionAnalyticsService';
import { sessionMetaStore } from '../../services/SessionMetaStore';
import { getSessionProvider } from '../../services/sessionProvider';
import type { SessionLoadProgress } from '../../types/api/sessions';
import type { SessionMeta, SessionMetaView } from '../../types/prefix-fingerprint';
import type { IPCRouter } from '../IPCRouter';

export function registerSessionsHandlers(router: IPCRouter): void {
  const broadcastProgress = (progress: SessionLoadProgress) => {
    for (const win of BrowserWindow.getAllWindows()) {
      win.webContents.send('sessions:load-progress', progress);
    }
  };

  // --- Lightweight project folder listing ---

  router.handle('list-project-folders', async (_event, toolId: string) => {
    const provider = getSessionProvider(toolId);
    return provider.listProjectFolders(broadcastProgress);
  });

  router.handle('get-latest-session-meta', async (_event, toolId: string, projectPath: string) => {
    const provider = getSessionProvider(toolId);
    return provider.getLatestSessionMeta(projectPath);
  });

  // --- Multi-CLI session endpoints (toolId-aware) ---

  router.handle(
    'get-all-projects-by-tool',
    async (_event, toolId: string, page: number, pageSize: number) => {
      const provider = getSessionProvider(toolId);
      return provider.getAllProjectsPaginated(page, pageSize, broadcastProgress);
    },
  );

  router.handle(
    'get-sessions-by-tool',
    async (_event, toolId: string, projectPath: string, page: number, pageSize: number) => {
      const provider = getSessionProvider(toolId);
      return provider.getProjectSessionsPaginated(projectPath, page, pageSize);
    },
  );

  router.handle(
    'read-log-by-tool',
    async (_event, toolId: string, projectPath: string, sessionId: string) => {
      const provider = getSessionProvider(toolId);
      return provider.readSessionLog(projectPath, sessionId);
    },
  );

  // --- Legacy Claude-only endpoints (backward compatible) ---

  router.handle('get-all-projects', async () => {
    return getAllClaudeProjects();
  });

  router.handle('get-total-count', async () => {
    return getTotalProjectCount();
  });

  router.handle('get-all-projects-paginated', async (_event, page: number, pageSize: number) => {
    return getAllClaudeProjectsPaginated(page, pageSize);
  });

  router.handle('get-project-sessions', async (_event, projectPath: string) => {
    return getProjectSessions(projectPath);
  });

  router.handle('get-project-sessions-basic', async (_event, projectPath: string) => {
    return getProjectSessionsBasic(projectPath);
  });

  router.handle('get-session-metadata', async (_event, projectPath: string, sessionId: string) => {
    return getSessionMetadata(projectPath, sessionId);
  });

  router.handle(
    'get-paginated',
    async (_event, projectPath: string, page: number, pageSize: number) => {
      return getProjectSessionsPaginated(projectPath, page, pageSize);
    },
  );

  router.handle('get-count', async (_event, projectPath: string) => {
    return getProjectSessionCount(projectPath);
  });

  router.handle('read-log', async (_event, projectPath: string, sessionId: string) => {
    return readSessionLog(projectPath, sessionId);
  });

  router.handle('get-summary', async (_event, projectPath: string, sessionId: string) => {
    return getSessionSummary(projectPath, sessionId);
  });

  router.handle('get-preview', async (_event, projectPath: string, sessionId: string) => {
    return getSessionPreview(projectPath, sessionId);
  });

  router.handle('get-user-questions', async (_event, projectPath: string, sessionId: string) => {
    return getUserQuestions(projectPath, sessionId);
  });

  router.handle(
    'get-auto-generated-requests',
    async (_event, projectPath: string, sessionId: string) => {
      return getAutoGeneratedRequests(projectPath, sessionId);
    },
  );

  router.handle('get-session-analysis', async (_event, projectPath: string, sessionId: string) => {
    const userQuestions = getUserQuestions(projectPath, sessionId);
    const autoGeneratedRequests = getAutoGeneratedRequests(projectPath, sessionId);
    const meta = sessionMetaStore.read(projectPath, sessionId);
    return { userQuestions, autoGeneratedRequests, meta };
  });

  router.handle('get-session-meta', async (_event, projectPath: string, sessionId: string) => {
    return sessionMetaStore.read(projectPath, sessionId);
  });

  router.handle('get-project-session-metas', async (_event, projectPath: string) => {
    const dir = getClaudeProjectDir(projectPath);
    const metas = sessionMetaStore.readAllInProjectDir(dir);
    const out: Record<string, SessionMeta> = {};
    for (const [id, meta] of metas) {
      out[id] = meta;
    }
    return out;
  });

  router.handle('get-project-session-views', async (_event, projectPath: string) => {
    const dir = getClaudeProjectDir(projectPath);
    const sidecars = sessionMetaStore.readAllInProjectDir(dir);
    const views: Record<string, SessionMetaView> = {};

    // Sidecars first (higher fidelity). Their session ids are then skipped
    // during derivation so we don't duplicate work.
    for (const [sessionId, meta] of sidecars) {
      views[sessionId] = {
        source: 'sidecar',
        sessionId,
        fingerprintHash: meta.fingerprint.static.total,
        metrics: meta.metrics,
        drift: meta.fingerprint.drift,
        // Project mcpResolved onto the view, dropping canonicalJson (too large
        // for a list). The renderer gets everything it needs to display the
        // enabled set, the baseline, and the override delta.
        mcpResolved: meta.mcpResolved
          ? {
              enabledServerIds: meta.mcpResolved.enabledServerIds,
              hash: meta.mcpResolved.hash,
              baselineServerIds: meta.mcpResolved.baselineServerIds,
              overrideAdd: meta.mcpResolved.overrideAdd,
              overrideRemove: meta.mcpResolved.overrideRemove,
            }
          : undefined,
      };
    }

    const skipIds = new Set(sidecars.keys());
    const derived = sessionAnalyticsService.analyzeProjectDir(dir, {
      skipSessionIds: skipIds,
      onProgress: (p) => {
        broadcastProgress({
          phase: p.phase === 'done' ? 'done' : 'reading',
          current: p.current,
          total: p.total,
          message: p.message,
        });
      },
    });
    for (const [sessionId, meta] of derived) {
      views[sessionId] = {
        source: 'derived',
        sessionId,
        fingerprintHash: meta.fingerprintHash,
        metrics: meta.metrics,
        model: meta.details.model ?? undefined,
        toolCount: meta.details.tools.length,
      };
    }

    return views;
  });

  router.handle('open-logs-folder', async () => {
    const logsDir = getClaudeProjectsDir();
    await shell.openPath(logsDir);
  });

  router.handle('open-project-folder', async (_event, projectPath: string) => {
    await shell.openPath(projectPath);
  });
}
