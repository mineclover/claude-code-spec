/**
 * SessionsAPI type contract
 */

import type { SessionMeta, SessionMetaView } from '../prefix-fingerprint';

export interface ClaudeProjectInfo {
  projectPath: string;
  projectDirName: string;
  claudeProjectDir: string;
  sessions: ClaudeSessionInfo[];
}

export interface ClaudeSessionInfo {
  sessionId: string;
  filePath: string;
  fileSize: number;
  lastModified: number;
  cwd?: string;
  firstUserMessage?: string;
  hasData: boolean;
}

export interface ClaudeSessionEntry {
  type: string;
  summary?: string;
  leafUuid?: string;
  [key: string]: unknown;
}

export interface ProjectFolder {
  projectPath: string; // 실제 파일시스템 경로
  projectDirName: string; // 디렉토리 이름
  lastModified?: number; // 가장 최근 세션 파일의 mtime (stat 기반)
  sessionCount?: number; // 세션 파일 수 (readdir 기반, optional)
}

export interface LatestSessionMeta {
  sessionId: string;
  lastModified: number;
  fileSize: number;
  cwd?: string;
  firstUserMessage?: string;
  hasData: boolean;
}

export interface SessionLoadProgress {
  phase: 'scanning' | 'reading' | 'processing' | 'done';
  current?: number;
  total?: number;
  message?: string;
}

export interface SessionsAPI {
  onLoadProgress: (callback: (progress: SessionLoadProgress) => void) => () => void;
  // Lightweight project folder listing
  listProjectFolders: (toolId: string) => Promise<ProjectFolder[]>;
  getLatestSessionMeta: (toolId: string, projectPath: string) => Promise<LatestSessionMeta | null>;

  // Multi-CLI endpoints (toolId-aware)
  getAllProjectsByTool: (
    toolId: string,
    page: number,
    pageSize: number,
  ) => Promise<{
    projects: ClaudeProjectInfo[];
    total: number;
    hasMore: boolean;
  }>;
  getSessionsByTool: (
    toolId: string,
    projectPath: string,
    page: number,
    pageSize: number,
  ) => Promise<{
    sessions: Omit<ClaudeSessionInfo, 'cwd' | 'firstUserMessage' | 'hasData'>[];
    total: number;
    hasMore: boolean;
  }>;
  readLogByTool: (
    toolId: string,
    projectPath: string,
    sessionId: string,
  ) => Promise<ClaudeSessionEntry[]>;

  // Legacy Claude-only endpoints
  getAllProjects: () => Promise<ClaudeProjectInfo[]>;
  getTotalCount: () => Promise<number>;
  getAllProjectsPaginated: (
    page: number,
    pageSize: number,
  ) => Promise<{
    projects: ClaudeProjectInfo[];
    total: number;
    hasMore: boolean;
  }>;
  getProjectSessions: (projectPath: string) => Promise<ClaudeSessionInfo[]>;
  getProjectSessionsBasic: (
    projectPath: string,
  ) => Promise<Omit<ClaudeSessionInfo, 'cwd' | 'firstUserMessage' | 'hasData'>[]>;
  getProjectSessionsPaginated: (
    projectPath: string,
    page: number,
    pageSize: number,
  ) => Promise<{
    sessions: Omit<ClaudeSessionInfo, 'cwd' | 'firstUserMessage' | 'hasData'>[];
    total: number;
    hasMore: boolean;
  }>;
  getProjectSessionCount: (projectPath: string) => Promise<number>;
  getSessionMetadata: (
    projectPath: string,
    sessionId: string,
  ) => Promise<Pick<ClaudeSessionInfo, 'cwd' | 'firstUserMessage' | 'hasData'>>;
  readLog: (projectPath: string, sessionId: string) => Promise<ClaudeSessionEntry[]>;
  getSummary: (projectPath: string, sessionId: string) => Promise<string | null>;
  getPreview: (projectPath: string, sessionId: string) => Promise<string | null>;
  getUserQuestions: (projectPath: string, sessionId: string) => Promise<ClaudeSessionEntry[]>;
  getAutoGeneratedRequests: (
    projectPath: string,
    sessionId: string,
  ) => Promise<ClaudeSessionEntry[]>;
  getSessionAnalysis: (
    projectPath: string,
    sessionId: string,
  ) => Promise<{
    userQuestions: ClaudeSessionEntry[];
    autoGeneratedRequests: ClaudeSessionEntry[];
    meta: SessionMeta | null;
  }>;
  /** Sidecar prefix-fingerprint + cache metrics for a single session. */
  getSessionMeta: (projectPath: string, sessionId: string) => Promise<SessionMeta | null>;
  /** All sidecars for the project, keyed by sessionId. Used by list UIs. */
  getProjectSessionMetas: (projectPath: string) => Promise<Record<string, SessionMeta>>;
  /**
   * Unified per-session view keyed by sessionId. Prefers sidecar when present,
   * otherwise derives metrics + grouping hash from the session's JSONL log.
   * This is what the Sessions UI should consume for the grouping indicator.
   */
  getProjectSessionViews: (projectPath: string) => Promise<Record<string, SessionMetaView>>;
  openLogsFolder: () => Promise<void>;
  openProjectFolder: (projectPath: string) => Promise<void>;
}
