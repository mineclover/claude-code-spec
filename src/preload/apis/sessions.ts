/**
 * Sessions preload API
 */

import { contextBridge, ipcRenderer } from 'electron';
import type { SessionLoadProgress, SessionsAPI } from '../../types/api';

export function exposeSessionsAPI(): void {
  contextBridge.exposeInMainWorld('sessionsAPI', {
    // Progress events
    onLoadProgress: (callback: (progress: SessionLoadProgress) => void) => {
      const handler = (_event: Electron.IpcRendererEvent, progress: SessionLoadProgress) => {
        callback(progress);
      };
      ipcRenderer.on('sessions:load-progress', handler);
      return () => ipcRenderer.removeListener('sessions:load-progress', handler);
    },

    // Lightweight project folder listing
    listProjectFolders: (toolId: string) =>
      ipcRenderer.invoke('sessions:list-project-folders', toolId),
    getLatestSessionMeta: (toolId: string, projectPath: string) =>
      ipcRenderer.invoke('sessions:get-latest-session-meta', toolId, projectPath),

    // Multi-CLI endpoints
    getAllProjectsByTool: (toolId: string, page: number, pageSize: number) =>
      ipcRenderer.invoke('sessions:get-all-projects-by-tool', toolId, page, pageSize),
    getSessionsByTool: (toolId: string, projectPath: string, page: number, pageSize: number) =>
      ipcRenderer.invoke('sessions:get-sessions-by-tool', toolId, projectPath, page, pageSize),
    readLogByTool: (toolId: string, projectPath: string, sessionId: string) =>
      ipcRenderer.invoke('sessions:read-log-by-tool', toolId, projectPath, sessionId),

    // Legacy Claude-only endpoints
    getAllProjects: () => ipcRenderer.invoke('sessions:get-all-projects'),
    getTotalCount: () => ipcRenderer.invoke('sessions:get-total-count'),
    getAllProjectsPaginated: (page: number, pageSize: number) =>
      ipcRenderer.invoke('sessions:get-all-projects-paginated', page, pageSize),
    getProjectSessions: (projectPath: string) =>
      ipcRenderer.invoke('sessions:get-project-sessions', projectPath),
    getProjectSessionsBasic: (projectPath: string) =>
      ipcRenderer.invoke('sessions:get-project-sessions-basic', projectPath),
    getProjectSessionsPaginated: (projectPath: string, page: number, pageSize: number) =>
      ipcRenderer.invoke('sessions:get-paginated', projectPath, page, pageSize),
    getProjectSessionCount: (projectPath: string) =>
      ipcRenderer.invoke('sessions:get-count', projectPath),
    getSessionMetadata: (projectPath: string, sessionId: string) =>
      ipcRenderer.invoke('sessions:get-session-metadata', projectPath, sessionId),
    readLog: (projectPath: string, sessionId: string) =>
      ipcRenderer.invoke('sessions:read-log', projectPath, sessionId),
    getSummary: (projectPath: string, sessionId: string) =>
      ipcRenderer.invoke('sessions:get-summary', projectPath, sessionId),
    getPreview: (projectPath: string, sessionId: string) =>
      ipcRenderer.invoke('sessions:get-preview', projectPath, sessionId),
    getUserQuestions: (projectPath: string, sessionId: string) =>
      ipcRenderer.invoke('sessions:get-user-questions', projectPath, sessionId),
    getAutoGeneratedRequests: (projectPath: string, sessionId: string) =>
      ipcRenderer.invoke('sessions:get-auto-generated-requests', projectPath, sessionId),
    getSessionAnalysis: (projectPath: string, sessionId: string) =>
      ipcRenderer.invoke('sessions:get-session-analysis', projectPath, sessionId),
    openLogsFolder: () => ipcRenderer.invoke('sessions:open-logs-folder'),
    openProjectFolder: (projectPath: string) =>
      ipcRenderer.invoke('sessions:open-project-folder', projectPath),
  } as SessionsAPI);
}
